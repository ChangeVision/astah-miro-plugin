package com.change_vision.astah.lab.plugin.miro.widget;

public class Id {
    protected String id;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "{" +
                "id:'" + id + '\'' +
                '}';
    }

}
